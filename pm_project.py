import pandas as pd
import numpy as np

# Configuration
TARGET_ROWS = 21000
df = pd.read_csv('2_Data_actual_cost_time.csv')
project_col = 'Project_Type'

# --- FIX 1: UPDATE COLUMN CATEGORIZATION ---
# 'Scope_Complexity_Numeric' is removed because it no longer exists in your CSV
discrete_cols = []
percentage_cols = ['Risk_Assessment_Score', 'Resource_Allocation_Score']
linear_cols = ['Engineered_Area', 'Actual_Cost_Total', 'Actual_Time_Total', 'Time_Estimate_Days']

# Automatically identify all other columns (categorical/one-hot dummies)
all_cols = df.columns.tolist()
processed_cols = [project_col] + discrete_cols + percentage_cols + linear_cols
# This list now correctly includes 'Comp_Low', 'Comp_Medium', 'Comp_High'
remaining_cols = [c for c in all_cols if c not in processed_cols]

# 1. Maintain Original Class Distribution
weights = df[project_col].value_counts(normalize=True)
needed_count = TARGET_ROWS - len(df)
synthetic_data = []

print(f"Starting augmentation. Generating {needed_count} new rows...")

for i in range(needed_count):
    p_type = np.random.choice(weights.index, p=weights.values)
    subset = df[df[project_col] == p_type]

    # Select parents
    idx = subset.index
    row1, row2 = subset.loc[np.random.choice(idx)], subset.loc[np.random.choice(idx)]

    # Interpolation factor
    alpha = np.random.uniform(-0.05, 1.05)
    new_row = {project_col: p_type}

    # 2. Process Numeric Columns
    for col in linear_cols + percentage_cols + discrete_cols:
        val = (row1[col] * alpha) + (row2[col] * (1 - alpha))

        if col in percentage_cols:
            noise = np.random.normal(0, 2.0)
            new_row[col] = np.clip(val + noise, 0, 100)
        else:
            # Add small noise to linear values
            noise_level = 0.02 if i % 10 != 0 else 0.05
            noise = np.random.normal(0, abs(noise_level * val))
            new_row[col] = val + noise

    # 3. Process Remaining Columns (One-Hot Dummies & Categorical)
    # We pick from ONE parent to ensure categorical integrity
    # (Fixes the ValueError: a must be 1-dimensional)
    if np.random.rand() < 0.5:
        chosen_parent = row1
    else:
        chosen_parent = row2

    for col in remaining_cols:
        new_row[col] = chosen_parent[col]

    synthetic_data.append(new_row)

# 4. Final Assembly
df_synthetic = pd.DataFrame(synthetic_data)
df_final = pd.concat([df, df_synthetic], ignore_index=True)

# Ensure physical constraints
for col in ['Engineered_Area', 'Actual_Cost_Total']:
    if col in df_final.columns:
        df_final[col] = df_final[col].clip(lower=df[col].min() * 0.8)

# Verification
print(f"Augmentation complete. Total rows: {len(df_final)}")
