def get_forecast():
    # 1. Encodings
    proj_vec = [0] * len(assets['encoded_project_cols'])
    try:
        proj_idx = assets['encoded_project_cols'].index(f"Proj_{proj_type.value}")
        proj_vec[proj_idx] = 1
    except: pass

    comp_vec = [0] * len(assets['encoded_complexity_cols'])
    c_map = {1: 'Comp_Low', 2: 'Comp_Medium', 3: 'Comp_High'}
    try:
        comp_idx = assets['encoded_complexity_cols'].index(c_map[complexity.value])
        comp_vec[comp_idx] = 1
    except: pass

    # 2. Engineered Features
    overrun_adj = assets.get('global_overrun_avg', 1.0)
    internal_names = ['Site_Prep_Actual_Cost', 'Foundations_Actual_Cost', 'Structure_Actual_Cost', 'Systems_Actual_Cost', 'Finishing_Actual_Cost']

    e_costs, e_risks = [], []
    for i in range(5):
        e_costs.append(total_cost.value * assets['avg_cost_weights'][i] * overrun_adj)
        risk_factor = assets['type_risk_distribution'][internal_names[i]].get(proj_type.value, 0.2)
        e_risks.append(risk_score.value * risk_factor)

    # 3. Assemble and Predict
    core_cont = [area_m2.value, res_score.value, total_time.value]
    full_input = proj_vec + comp_vec + core_cont + e_costs + e_risks
    feat = (np.array(full_input) - assets['params_min']) / assets['params_range']
    pred = model(jnp.array([feat]))[0]
    return np.array(pred) * assets['output_range'] + assets['output_min']

