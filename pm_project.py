import pandas as pd

# --- CONFIGURATION ---
STAGES = ['Site_Prep', 'Foundations', 'Structure', 'Systems', 'Finishing']

# Updated internal weights for 3 levels (Simulation logic)
# High has a stronger impact since 'Extreme' was removed
COMPLEXITY_WEIGHTS = {
    'Low': 0.2,
    'Medium': 0.6,
    'High': 1.1
}

def map_complexity_label(score):
    """Maps the numeric score 1, 2, 3 to specific labels."""
    # We use 1.5 and 2.5 as midpoints to handle any floating point noise
    if score < 1.5:
        return 'Low'
    elif score < 2.5:
        return 'Medium'
    else:
        return 'High'

# --- PROCESSING ---
# Load the output from Part 1
df_planned = pd.read_csv('1_Data_by_stages.csv')
final_list = []

print("Simulating execution with 3-level Complexity (Low, Medium, High)...")

for _, original_row in df_planned.iterrows():
    row = original_row.to_dict()

    # 1. Define Complexity Category and Factor
    comp_score = float(row['Scope_Complexity_Numeric'])
    comp_label = map_complexity_label(comp_score)
    comp_factor = COMPLEXITY_WEIGHTS[comp_label]

    hist_dev = float(row['Historical_Cost_Deviation_%']) / 100
    resources = float(row.get('Resources', 5))

    actual_cost_total = 0
    actual_time_total = 0

    # 2. CALCULATE ACTUALS PER STAGE
    for stage in STAGES:
        p_cost = row[f'{stage}_Planned_Cost']
        p_days = row[f'{stage}_Planned_Days']
        s_risk = row[f'{stage}_Stage_Risk']

        # Cost Actual Formula: Impact of Area, Risk, and Complexity Factor
        # Note: Engineered_Area is used here as a slight overhead multiplier
        area_multiplier = 1 + (row['Engineered_Area'] / 1000000) # Minor scale impact
        cost_variance = (1 + hist_dev) * (1 + (comp_factor * 0.05)) * (1 + (s_risk / 1000)) * area_multiplier
        res_efficiency = 1 - (resources * 0.01)

        a_cost = round(p_cost * cost_variance * res_efficiency, 2)

        # Time Actual Formula
        time_variance = (1 + (s_risk / 150)) * (1 + (comp_factor * 0.08))
        res_delay = 1.3 - (resources / 10)
        a_days = int(round(p_days * time_variance * res_delay))

        row[f'{stage}_Actual_Cost'] = a_cost
        row[f'{stage}_Actual_Days'] = a_days

        actual_cost_total += a_cost
        actual_time_total += a_days

    # 3. ASSIGN TOTALS AND LABEL
    row['Actual_Cost_Total'] = round(actual_cost_total, 2)
    row['Actual_Time_Total'] = actual_time_total
    row['Complexity_Level'] = comp_label

    final_list.append(row)

# --- FINAL TRANSFORMATION & ONE-HOT ---
df_final = pd.DataFrame(final_list)

# Perform One-Hot Encoding for the 3 levels
