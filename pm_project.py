import jax
import jax.numpy as jnp
import numpy as np
import pandas as pd
import optax
import matplotlib.pyplot as plt
from flax import nnx
import joblib
import time

# --- 1. DATA PREPARATION ---
df = pd.read_csv('3_Data_21ksamples.csv')

cost_stages = ['Site_Prep_Actual_Cost', 'Foundations_Actual_Cost', 'Structure_Actual_Cost', 'Systems_Actual_Cost', 'Finishing_Actual_Cost']
time_stages = ['Site_Prep_Actual_Days', 'Foundations_Actual_Days', 'Structure_Actual_Days', 'Systems_Actual_Days', 'Finishing_Actual_Days']
output_cols = cost_stages + time_stages

# 1.1 Cleaning
cols_to_fix = ['Engineered_Area', 'Estimated_Cost_USD', 'Risk_Assessment_Score',
               'Resource_Allocation_Score', 'Time_Estimate_Days'] + output_cols
for col in cols_to_fix:
    df[col] = pd.to_numeric(df[col], errors='coerce')
df = df.replace([np.inf, -np.inf], np.nan).dropna(subset=cols_to_fix + ['Project_Type', 'Scope_Complexity'])

# --- 1.2 LOGIC INJECTION ---
total_actual = df[cost_stages].sum(axis=1)
df['Historical_Overrun_Ratio'] = total_actual / df['Estimated_Cost_USD']
global_overrun_avg = df['Historical_Overrun_Ratio'].mean()

avg_cost_weights = df[cost_stages].div(df[cost_stages].sum(axis=1), axis=0).mean()
avg_time_weights = df[time_stages].div(df[time_stages].sum(axis=1), axis=0).mean().values.tolist()

type_risk_dist = df.groupby('Project_Type')[cost_stages].mean()
type_risk_dist = type_risk_dist.div(type_risk_dist.sum(axis=1), axis=0)

for i in range(5):
    df[f'Est_Stage_Cost_{i}'] = df['Estimated_Cost_USD'] * avg_cost_weights.iloc[i] * global_overrun_avg
    df[f'Stage_Risk_{i}'] = df.apply(
        lambda row: row['Risk_Assessment_Score'] * type_risk_dist.loc[row['Project_Type']].iloc[i], axis=1
    )

engineered_inputs = [f'Est_Stage_Cost_{i}' for i in range(5)] + [f'Stage_Risk_{i}' for i in range(5)]
continuous_input_cols = ['Engineered_Area', 'Resource_Allocation_Score', 'Time_Estimate_Days'] + engineered_inputs

proj_dummies = pd.get_dummies(df['Project_Type'], prefix='Proj')
comp_dummies = pd.get_dummies(df['Scope_Complexity'], prefix='Comp')

X_raw = pd.concat([proj_dummies, comp_dummies, df[continuous_input_cols]], axis=1).values.astype(np.float32)
Y_raw = df[output_cols].values.astype(np.float32)
param_cols = pd.concat([proj_dummies, comp_dummies, df[continuous_input_cols]], axis=1).columns.tolist()

num_params, num_output = len(param_cols), len(output_cols)

# 1.4 Normalization
p_min, p_max = X_raw.min(axis=0), X_raw.max(axis=0)
o_min, o_max = Y_raw.min(axis=0), Y_raw.max(axis=0)
p_range = np.where((p_max - p_min) == 0, 1.0, p_max - p_min)
o_range = np.where((o_max - o_min) == 0, 1.0, o_max - o_min)

idx = int(len(X_raw) * 0.8)
X_train_norm, X_valid_norm = jnp.array((X_raw[:idx] - p_min) / p_range), jnp.array((X_raw[idx:] - p_min) / p_range)
Y_train_norm, Y_valid_norm = jnp.array((Y_raw[:idx] - o_min) / o_range), jnp.array((Y_raw[idx:] - o_min) / o_range)

# --- 2. MODEL WITH NON-NEGATIVE OUTPUT ---
class ConstructionNN(nnx.Module):
    def __init__(self, rngs: nnx.Rngs):
        self.linear1 = nnx.Linear(num_params, 128, rngs=rngs)
        self.linear2 = nnx.Linear(128, 64, rngs=rngs)
        self.linear3 = nnx.Linear(64, num_output, rngs=rngs)
    def __call__(self, x):
        x = nnx.silu(self.linear1(x))
        x = nnx.silu(self.linear2(x))
        x = self.linear3(x)
        # Softplus prevents negative numbers while allowing high positive values
        return jax.nn.softplus(x)

model = ConstructionNN(rngs=nnx.Rngs(42))
optimizer = nnx.Optimizer(model, wrt=nnx.Param, tx=optax.adam(5e-4))

@nnx.jit
def train_step(mod, opt, X, Y):
    def loss_fn(m):
        preds = m(X)
        diff = preds - Y
        # Penalty for under-prediction (diff < 0 means actual > pred)
        penalty = jnp.where(diff < 0, 4.0, 1.0)
        return jnp.mean(penalty * (diff**2))
    grads = nnx.grad(loss_fn)(mod)
    opt.update(mod, grads)

# --- 3. TRAINING LOOP ---
log_epochs, log_loss_train, log_loss_valid = [], [], []
print(f"🚀 Training for Overrun Accuracy...")
for epoch in range(10001):
    train_step(model, optimizer, X_train_norm, Y_train_norm)
    if epoch % 500 == 0:
        l_t = float(jnp.mean((model(X_train_norm) - Y_train_norm)**2))
        l_v = float(jnp.mean((model(X_valid_norm) - Y_valid_norm)**2))
        log_epochs.append(epoch); log_loss_train.append(l_t); log_loss_valid.append(l_v)
        print(f'Epoch: {epoch:>5} | Train MSE: {l_t:.3e} | Valid MSE: {l_v:.3e}')

# --- 4. EXPORT ---
# Compatibility Fix: Export state as pure dictionary of numpy arrays
# This avoids 'AttributeError: _raw_value' in different environments
pure_state = jax.tree_util.tree_map(
    lambda x: np.array(x.value) if hasattr(x, 'value') else np.array(x),
    nnx.state(model, nnx.Param)
)

joblib.dump({
    'model_state': pure_state,
    'param_cols': param_cols,
    'params_min': p_min, 'params_range': p_range,
    'output_min': o_min, 'output_range': o_range,
    'type_risk_distribution': type_risk_dist.to_dict(),
    'avg_cost_weights': avg_cost_weights.values.tolist(),
    'avg_time_weights': avg_time_weights,
    'global_overrun_avg': float(global_overrun_avg),
    'encoded_project_cols': [c for c in param_cols if c.startswith('Proj_')],
    'encoded_complexity_cols': [c for c in param_cols if c.startswith('Comp_')]
}, 'project_model.pkl')

# Also save the training history for visualization
np.savez(
    'validation_results.npz',
    log_epochs=np.array(log_epochs),
    log_train=np.array(log_loss_train),
    log_valid=np.array(log_loss_valid),
    true=np.array(Y_raw[idx:]), # Save true values for plotting
    pred=np.array(model(X_valid_norm)) * o_range + o_min, # Save denormalized predictions
    labels=np.array(output_cols) # Save output column names
)

